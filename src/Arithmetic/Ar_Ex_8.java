package Arithmetic;

public class Ar_Ex_8 {
    public static void main(String[] args) {
        for(int a = 1; a <= 10; a++){
            for(int i = 1; i <= 10; i++){
                System.out.print(i * a + " ");
            }
            System.out.println();
        }
    }
}
